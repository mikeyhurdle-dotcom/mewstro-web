const fs = require("fs");
const path = require("path");
const sharp = require("sharp");

const SRC = "/Users/mikey.gilson@tealium.com/StormlightArchive/Projects/Personal/HobbyTracker/Mewstro/Docs/Screenshots/6.9";
const OUT = "/Users/mikey.gilson@tealium.com/StormlightArchive/Projects/Personal/HobbyTracker/app-landings/src/components/mewstro/teacher-assets/screenshots.ts";

const items = [
  { key: "PRACTICE_TIMER", file: "01_practice_timer.png" },
  { key: "CALENDAR", file: "02_calendar.png" },
  { key: "MILESTONE", file: "07_milestone.png" },
];

(async () => {
  const lines = [
    "// Auto-generated by /tmp/gen-screenshots.js — do not edit by hand.",
    "// Resized device screenshots for embedding in PDFs.",
    "",
  ];
  for (const it of items) {
    const buf = await sharp(path.join(SRC, it.file))
      .resize({ width: 360, withoutEnlargement: true })
      .png({ compressionLevel: 9, palette: true })
      .toBuffer();
    const dataUrl = `data:image/png;base64,${buf.toString("base64")}`;
    lines.push(`export const SCREENSHOT_${it.key}_DATA_URL = "${dataUrl}";`);
    lines.push("");
    console.log(it.file, "→", (buf.length / 1024).toFixed(1) + "KB");
  }
  fs.writeFileSync(OUT, lines.join("\n"));
  console.log("wrote", OUT);
})();
